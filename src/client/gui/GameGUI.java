    private void drawPieces() {
        for (int i = 0; i < 8; i++) {
            for (int j = 0; j < 8; j++) {
                JButton b = cells[i][j];
//                b.setText(getPieceCode(pieces[i][j].getType()));
                b.setText(getPieceCode(pieces[i][j]));
            }
        }
    }

    private void makeButtons() {
        int c = 0;
        for (int i = 0; i < 8; i++) {
            for (int j = 0; j < 8; j++) {
                JButton b = new JButton();
                if (c == 0) {
                    b.setBackground(Color.WHITE);
                    b.setForeground(Color.BLACK);
                } else {
                    b.setBackground(Color.BLACK);
                    b.setForeground(Color.WHITE);
                }
                jPanel1.add(b);
                b.setMargin(new Insets(0, 0, 0, 0));
                b.setFont(new Font("Dialog", Font.PLAIN, 40));
                b.addActionListener((ActionEvent ae) -> {
                    int[] click = getButton((JButton) ae.getSource());
                    System.out.println(Arrays.toString(click));
                });
                cells[i][j] = b;
                c ^= 1;
            }
            c ^= 1;
        }
    }

    private void updateBoard(String[][] p) {
        pieces = p;
        drawPieces();
        repaint();
    }

    private int[] getButton(JButton b) {
        int[] click = new int[2];
        for (int i = 0; i < 8; i++) {
            for (int j = 0; j < 8; j++) {
                if (b.equals(cells[i][j])) {
                    click[0] = i;
                    click[1] = j;
                }
            }
        }
        return click;
    }

    private String getPieceCode(String p) {
        String code = null;
        switch (p) {
            case "KW":
                code = "\u2654";
                break;
            case "QW":
                code = "\u2655";
                break;
            case "RW":
                code = "\u2656";
                break;
            case "BW":
                code = "\u2657";
                break;
            case "HW":
                code = "\u2658";
                break;
            case "PW":
                code = "\u2659";
                break;

            case "KB":
                code = "\u265A";
                break;
            case "QB":
                code = "\u265B";
                break;
            case "RB":
                code = "\u265C";
                break;
            case "BB":
                code = "\u265D";
                break;
            case "HB":
                code = "\u265E";
                break;
            case "PB":
                code = "\u265F";
                break;
        }
        return code;
    }

